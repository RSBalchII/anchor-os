// 2. API LAYER (Request/Response Interfaces)
// =============================================================================

/**
 * Menu: Core memory representation for retrieval
 * 
 * Purpose: Standardized memory format for search and retrieval
 * Location: src/types/api.ts (enhanced)
 */
export interface Menu {
    id: string;
    content: string;
    source: string;
    type: string;
    timestamp: number;
    buckets: string[];
    tags: string;
    epochs: string;
    provenance: string;
    score?: number;
    semantic_categories?: string[]; // Added for semantic shift architecture
}

/**
 * SearchRequest: Query parameters for Tag-Walker protocol
 * 
 * Purpose: Defines search parameters and filters
 * Location: src/types/api.ts (enhanced)
 */
export interface SearchRequest {
    query: string;           // The natural language query
    limit?: number;          // Elastic Window (default 20)
    max_chars?: number;      // Character budget
    deep?: boolean;          // If true, trigger 'Epochal' search (Dreamer layers)

    // The "UniversalRAG" Routing Layer
    buckets?: string[];      // e.g., ["@code", "@visual", "@memory"]
    provenance?: 'internal' | 'external' | 'quarantine' | 'all'; // Data Provenance filter
    semantic_categories?: string[]; // Filter by semantic categories (Standard 084)
}

/**
 * SearchResponse: Results from Tag-Walker retrieval
 * 
 * Purpose: Standardized search response format
 * Location: src/types/api.ts (enhanced)
 */
export interface SearchResponse {
    context: string;
    results: Menu[];
    metadata: {
        engram_hits: number;   // Did we find exact entity matches?
        vector_latency: number;
        provenance_boost_active: boolean;
        semantic_categories_used?: string[]; // Categories that influenced results
    }
}

