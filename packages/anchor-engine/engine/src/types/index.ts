/**
 * Atom: The fundamental unit of semantic meaning
 * 
 * Purpose: Represents the smallest semantic unit (e.g., "#python", "UserAuthentication")
 * Location: src/types/atomic.ts (enhanced)
 */
export interface Atom {
    id: string;          // Unique Ref (hash of label)
    label: string;       // Human readable (e.g. "#python")
    type: 'concept' | 'entity' | 'keyword' | 'system' | 'relationship' | 'narrative' | 'technical' | 'industry' | 'location' | 'emotional' | 'temporal' | 'causal';
    weight: number;      // Importance (0-1)
    embedding?: number[]; // Semantic Vector
}

/**
 * Molecule: A coherent chain of atoms expressing specific intent
 * 
 * Purpose: Semantic segment with byte coordinates and relationship data
 * Location: src/types/atomic.ts (enhanced)
 */
export interface Molecule {
    id: string;
    content: string;     // The actual text (e.g. "Python is great.")
    atoms: string[];     // Pointers to Atom IDs present in this molecule
    sequence: number;    // Order within the Compound
    compoundId: string;  // Parent Compound

    // Universal Coordinates (Pointer-Based)
    start_byte: number;
    end_byte: number;

    // Type Distinction
    type: 'prose' | 'code' | 'data';

    // Quantitative Data (Optional)
    numeric_value?: number; // e.g. 1500.0
    numeric_unit?: string;  // e.g. "PSI"

    // Deduplication (SimHash)
    molecular_signature?: string;
    
    // Relationship Data
    entities?: string[];    // Related entities in this molecule
    relationships?: string[]; // Detected relationships (e.g., "#Relationship", "#Narrative")
}

/**
 * Compound: Stable aggregate of molecules (document-level entity)
 * 
 * Purpose: Document-level entity with full content and metadata
 * Location: src/types/atomic.ts (enhanced)
 */
export interface Compound {
    id: string;          // memory_id
    compound_body: string;     // Full text content (formerly content)
    molecules: string[]; // Pointers to Molecule IDs
    atoms: string[];     // Aggregate set of Atom IDs (formerly tags)

    // Metadata
    path: string;
    timestamp: number;
    provenance: 'internal' | 'external' | 'quarantine';
    molecular_signature: string; // 64-bit Hamming SimHash
    
    // Enhanced metadata for semantic shift
    semantic_categories?: string[]; // High-level semantic tags (e.g., "#Relationship", "#Technical")
    epochs?: string; // Temporal classification
    buckets: string[]; // Organizational containers
    tags: string; // Legacy tag system
}

